import pyglet as pg
from pyglet.gl import *

window = pg.window.Window(600, 600)
window.set_caption('Plane')


@window.event
def on_draw():
    glClearColor(0.0, 0.0, 0.5, 1.0)
    glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT)
    glMatrixMode(GL_PROJECTION)
    glLoadIdentity()
    glOrtho(-80.0, 80.0, -80.0, 80.0, -80.0, 80.0)
    glEnable(GL_LIGHTING)
    glEnable(GL_CULL_FACE)
    glFrontFace(GL_CCW)
    glEnable(GL_COLOR_MATERIAL)
    glEnable(GL_DEPTH_TEST)
    ambient_light = (GLfloat * 4)(*(1.0, 1.0, 1.0, 1.0))
    glLightModelfv(GL_LIGHT_MODEL_AMBIENT, ambient_light)
    glColorMaterial(GL_FRONT, GL_AMBIENT_AND_DIFFUSE)
    glMatrixMode(GL_MODELVIEW)
    glLoadIdentity()
    glPushMatrix()
    glRotatef(45.0, 1.0, 0.0, 0.0)
    glRotatef(60.0, 0.0, 1.0, 0.0)
    glRotatef(0.0, 0.0, 0.0, 1.0)
    glColor3f(1.0, 1.0, 1.0)
    glBegin(GL_TRIANGLES)
    glVertex3f(-15.0, 0.0, 30.0)
    glVertex3f(15.0, 0.0, 30.0)
    glVertex3f(0.0, 0.0, 60.0)
    glColor3f(0.0, 0.0, 0.0)
    glVertex3f(-15.0, 0.0, 30.0)
    glVertex3f(0.0, 0.0, 60.0)
    glVertex3f(0.0, 15.0, 30.0)
    glColor3f(1.0, 0.0, 0.0)
    glVertex3f(0.0, 0.0, 60.0)
    glVertex3f(15.0, 0.0, 30.0)
    glVertex3f(0.0, 15.0, 30.0)
    glVertex3f(0.0, 1.0, 0.0)
    glVertex3f(-15.0, 0.0, 30.0)
    glVertex3f(0.0, 15.0, 30.0)
    glVertex3f(0.0, 0.0, -53.0)
    glColor3f(1.0, 1.0, 1.0)
    glVertex3f(0.0, 15.0, 30.0)
    glVertex3f(15.0, 0.0, 30.0)
    glVertex3f(0.0, 0.0, -53.0)
    glColor3f(0.5, 0.5, 0.3)
    glVertex3f(15.0, 0.0, 30.0)
    glVertex3f(-15.0, 0.0, 30.0)
    glVertex3f(0.0, 0.0, -53.0)
    glColor3f(0.3, 1.0, 0.2)
    glVertex3f(0.0, 0.0, -53.0)
    glVertex3f(0.0, 0.0, -70.0)
    glVertex3f(0.0, 15.0, -70.0)
    glColor3f(0.7, 0.5, 0.3)
    glVertex3f(-15.0, 0.0, -70.0)
    glVertex3f(0.0, 0.0, -53.0)
    glVertex3f(0.0, 0.0, -70.0)
    glColor3f(0.2, 0.2, 0.8)
    glVertex3f(15.0, 0.0, -70.0)
    glVertex3f(0.0, 0.0, -70.0)
    glVertex3f(0.0, 0.0, -53.0)
    glColor3f(0.3, 1.0, 0.2)
    glVertex3f(0.0, 0.0, -70.0)
    glVertex3f(0.0, 0.0, -53.0)
    glVertex3f(0.0, 15.0, -70.0)
    glColor3f(0.7, 0.5, 0.3)
    glVertex3f(0.0, 0.0, -53.0)
    glVertex3f(-15.0, 0.0, -70.0)
